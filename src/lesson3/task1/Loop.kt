/**
 * Простая (2 балла)
 *
 * Найти число Фибоначчи из ряда 1, 1, 2, 3, 5, 8, 13, 21, ... с номером n.
 * Ряд Фибоначчи определён следующим образом: fib(1) = 1, fib(2) = 1, fib(n+2) = fib(n) + fib(n+1)
 */
fun fib(n: Int): Int {
    var fib_1 = 1
    var fib_2 = 1
    if (n in 1..2) return 1
    var fib_i = 0
    for (i in 3..n){
        fib_i = fib_1 + fib_2
        fib_1 = fib_2
        fib_2 = fib_i
    }
    return fib_i
}

